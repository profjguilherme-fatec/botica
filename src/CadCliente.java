    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       // Obtém o nome digitado no campo de texto
String nome = jTextField1.getText();

// Verifica se o campo está vazio
if (nome.isEmpty()) {
    JOptionPane.showMessageDialog(this, "O campo 'Nome' não pode estar vazio!", "Erro", JOptionPane.ERROR_MESSAGE);
    return;
}

// Converte o nome para letras maiúsculas
nome = nome.toUpperCase();

// Conecta ao banco de dados
Conexao conexao = new Conexao();
conexao.conectar();

String sqlVerifica = "SELECT COUNT(*) FROM cliente WHERE nome = ?";
String sqlInsert = "INSERT INTO cliente (nome) VALUES (?)";

try (Connection con = conexao.getConnection()) {
    // Verifica se o nome já está cadastrado
    try (PreparedStatement stmtVerifica = con.prepareStatement(sqlVerifica)) {
        stmtVerifica.setString(1, nome);
        ResultSet rs = stmtVerifica.executeQuery();
        if (rs.next() && rs.getInt(1) > 0) {
            JOptionPane.showMessageDialog(this, "Cliente já cadastrado!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }

    // Insere o cliente no banco de dados
    try (PreparedStatement stmtInsert = con.prepareStatement(sqlInsert)) {
        stmtInsert.setString(1, nome);
        stmtInsert.executeUpdate();
        JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!");
        dispose(); // Fecha a tela atual
    }
} catch (SQLException ex) {
    JOptionPane.showMessageDialog(this, "Erro ao acessar o banco de dados: " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
} finally {
    conexao.desconectar();
}

    }//GEN-LAST:event_jButton1ActionPerformed

